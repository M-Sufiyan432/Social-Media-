import React from "react";
import logo from "../assets/logo.png";
import { FaRegHeart } from "react-icons/fa6";
import db from "../assets/dp.webp";
import { useDispatch, useSelector } from "react-redux";
import axios from "axios"
import { serverURL } from "../App";
import { setUserData } from "../redux/userSlice";
import  OtherUsers  from "./OtherUsers";


const LeftHome = () => {
   const dispatch = useDispatch()
  const handleLogOut = async()=>{
      try {
        const result = await axios.get(`${serverURL}/api/auth/signout`,{withCredentials:true})
        console.log(result.data);
        dispatch(setUserData(null))
      } catch (error) {
        console.log("Handle Log Out Error",error?.message);
      }
  }
  
  const { userData } = useSelector((state) => state.user);
  const {suggestedUser} = useSelector((state)=>state.user)
  
  return (
    <div className="w-[25%] hidden lg:block min-h-[100vh] bg-black border-r-2 border-gray-900">
      <div className="w-full h-[100px]  flex items-center justify-between p-[20px]">
        <img src={logo} alt="" className="w-[80px]" />
        <div>
          <FaRegHeart className="text-[white] w-[25px] h-[25px]" />
        </div>
      </div>
      <div className="flex items-center w-full justify-between gap-[10px] px-[20px]  border-b-2 border-b-gray-900 py-[10px]">
        <div className="flex items-center gap-[10px]">
        <div className="w-[70px] h-[70px] border-2 border-black rounded-full cursor-pointer overflow-hidden">
          <img src={userData.profileImage || db} className="w-full object-cover" />
        </div>
        <div >
            <div className="text-[18px] text-white font-semibold">{userData.username}</div>
            <div className="text-[15px] text-gray-400 font-semibold">{userData.name}</div>
        </div>
        </div>
        <div className="text-blue-500 font-semibold cursor-pointer" onClick={handleLogOut}>Log Out</div>
      </div>
       
    <div className="w-full flex flex-col gap-[20px] p-[20px]">
      <h1 className="text-[white] text-[19px]">Suggested Users</h1>
       {suggestedUser && suggestedUser.slice(0,3).map((user,index)=>
            <OtherUsers key={index} user={user} />
       )}
    </div>

    </div>
  );
};

export default LeftHome;
